const os  = require('os');

// *Get  Platform [operating systems]
console.log(os.platform());


// * CPU architechture
console.log(os.arch())


// * CPU Core Info
console.log(os.cpus())


// * Free memory
console.log(os.freemem());


// * Total memory
console.log(os.totalmem());

// * Home directory
console.log(os.homedir());



// * Uptime -> the amount of time that your system has been up.
console.log(os.uptime());


